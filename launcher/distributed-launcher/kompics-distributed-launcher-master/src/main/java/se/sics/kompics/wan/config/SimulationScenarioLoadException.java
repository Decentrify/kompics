package se.sics.kompics.simulator;

public class SimulationScenarioLoadException extends Exception {


	private static final long serialVersionUID = 530024753206996860L;


	public SimulationScenarioLoadException(String arg0) {
		super(arg0);
	}

	public SimulationScenarioLoadException(Throwable arg0) {
		super(arg0);
	}

	public SimulationScenarioLoadException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
